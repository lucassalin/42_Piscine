char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	if (str[i] >= 'a' && str[i] <= 'z')
		str[i++] -= 32;
	while (str[i])
	{
		if (str[i - 1] < 48 || (str[i - 1] > 57 && str[i - 1] < 65) \
			|| str[i - 1] > 122 || (str[i -1] < 97 && str[i - 1] > 90))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] -= 32;
		}
		else if (str[i] >= 'A' && str[i] <= 'Z')
				str[i] += 32;
		i++;
	}
	return (str);
}
